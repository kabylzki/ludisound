                <p>
                    Il existe plusieurs types d'orchestre, tels que l'orchestre symphonique, l'orchestre philharmonique ou l'orchestre d'harmonie. La différence entre ces orchestres-ci est principalement par le type d'instruments qui les composent. 
                    Le symphonique par exemple, est constitué de quatre familles d'instruments, soit les cuivres, les bois (comme la flûte traversière utilisée pour le concerto pour flûte et harpe de Mozart), les cordes et les percussions, tandis que l'orchestre d'harmonie n'est constitué que de bois, de cuivres et de percussions tel que la batterie. 
